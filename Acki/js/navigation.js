// Navigation used by every page.
// Absolute paths keep links working from index.html and /page/*.html.

function goHome() {
    window.location.href = "/index.html";
}

function goAbout() {
    window.location.href = "/page/about.html";
}

function goContact() {
    window.location.href = "/page/contact.html";
}

function goCommunity() {
    window.location.href = "/page/community.html";
}

function goGoogle() {
    window.location.href = "https://www.google.com";
}
